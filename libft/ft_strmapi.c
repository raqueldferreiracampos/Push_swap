#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*str;
	size_t	len_s;
	size_t	i;

	if (!s || !f)
		return (NULL);
	i = 0;
	len_s = ft_strlen(s);
	str = malloc (sizeof(char) * (len_s + 1));
	if (!str)
		return (NULL);
	while (s[i])
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
