#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long int	nb;
	char		nb1;

	nb = n;
	if (nb < 0)
	{
		write(fd, "-", 1);
		nb *= -1;
	}
	if (nb > 9)
		ft_putnbr_fd(nb / 10, fd);
	nb1 = (nb % 10) + '0';
	write(fd, &nb1, 1);
}
